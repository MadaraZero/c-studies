#include <stdio.h>

int main()
{
  // two variables of type int that contain integers
  int age = 10;
  int height = 72;

  // using the printf function to print to screen
  // the variables are formatted with %d for int type variables
  printf("I am %d years old.\n", age);
  printf("I am %d inches tall.\n", height);

  // well well what do we have here ...
  return 0;
}
